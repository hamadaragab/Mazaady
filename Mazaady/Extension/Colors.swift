import Foundation
import UIKit
extension UIColor {
    static var DarkColor: UIColor {
        return UIColor(named: "DarkColor") ?? UIColor.blue
    }
    static var mainColor: UIColor {
        return UIColor(named: "mainColor") ?? UIColor.blue
    }
}
